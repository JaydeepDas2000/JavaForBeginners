package com.codewithjaydeep;

public class Main {

    public static void main(String[] args) {
        final float PI = 3.14F; // if we type final here java compiler will treat this as a constant so once we
        // initialize this we cannot change its value later on. by convention we use all capital letters to define
        // a constant
        System.out.println(PI);
    }
}

