package com.codewithjaydeep;

public class Main {

    public static void main(String[] args) {
        // to create quotes we use \"
        System.out.println("Hello \"Jaydeep\"");
        // to create backslash we use \\
        System.out.println("C:\\Windows\\...");
        // to create a new line we use \n
        System.out.println("Hello \nJaydeep in new line");
        // to create tab
        System.out.println("Hello \tJaydeep after tab");
    }
}
/*
*       --Escape Sequences--
*   sometime we need to include some kind of special char in string like a tab, a new line, a backslash or double quotes
* */